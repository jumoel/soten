export function Icon({
  name,
  size = "5",
  spin = false,
  className,
  "aria-hidden": ariaHidden = true,
}: IconProps) {
  const classes = [
    "inline-flex items-center justify-center",
    sizeClasses[size],
    spin ? "animate-spin" : "",
    className ?? "",
  ]
    .filter(Boolean)
    .join(" ");

  return (
    <span
      className={classes}
      aria-hidden={ariaHidden}
      style={spin ? { animationDuration: "0.8s" } : undefined}
    >
      {icons[name]}
    </span>
  );
}
